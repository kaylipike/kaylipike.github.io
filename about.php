        <h2>China's Demographics</h2> 
        <p>China is incredibly populated. Around one-third of the population resides in cities, with the rest living in the country. The birth rate in China has been plummeting for years, largely due to the one-child policy that was in place from 1980 to 2015. As a result of this policy there is an imbalance between males and females in China, as boys were more desired if only one child could be kept. Also, it is becoming more clear that there are far more people aging out of the workforce who must now be supported by a far smaller number of young people who were born during the time of the policy. In addition to that, childcare and education costs are very high and are discouraging people away from having children. Many are instead choosing to focus on their careers. The government is now trying to incentivize people to have more children, but have not had much success at this point.</p>
