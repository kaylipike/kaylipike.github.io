        <h2>About the Great Wall of China</h2> 
        <p>In Mandarin Chinese the 'Great Wall of China' is 万里长城. This means '10,000 <em>li</em> Great Wall' (one <em>li</em> is 500 meters long and the entire Great Wall is more than 21,000 kilometers). The Great Wall actually consists of many walls often parallel to each other. It is not one unbroken structure. The wall traces crestlines of mountains and hills and goes through the countryside. Around one fourth of the wall is made up of entirely natural barriers like rivers and mountain ridges. About 70% of the Great Wall is constructed wall and then the remaining amount is ditches or moats. Some of the wall is now in ruins or gone completely. It goes through 15 provinces, 97 prefectures, and 404 counties.</p>
