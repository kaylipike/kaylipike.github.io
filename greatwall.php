        <h2>Why was it built?</h2> 
        <p>The Great Wall developed over a long period of time. Much of it was built from an effort of individual Chinese kingdoms to fortify themselves from neighbors and barbarian invaders alike. Over history it was continuously built for three main purposes: “to protect the Chinese Empire from foreign invaders, act as a barrier between northern and southern civilizations, and protect the Silk Road Trade.” It was built by 19 dynasties over 2000 years, from 7th-century BCE to 16th CE. The credit for the Great Wall goes to the first emperor of the Qin dynasty, Qin Shi Huang. He is also referred to as the First Emperor. He united China around 220 BCE and put together the idea to unite all the existing walls into one.</p>
